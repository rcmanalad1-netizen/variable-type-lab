function getStyleCategory(pack: MoodPack, index: number): ConcreteStyleCategory {
  if (pack.mood === "Streetwear") {
    return index % 3 === 0 ? "Underground" : "Brutalist";
  }

  if (pack.mood === "Tech Signal" || pack.mood === "STEM Meme") {
    return index % 4 === 0 ? "Minimal" : "Futuristic";
  }

  if (pack.mood === "Emotional / Reflective" || pack.mood === "Faith") {
    return index % 5 === 0 ? "Editorial" : "Emotional";
  }

  return pack.category;
}
